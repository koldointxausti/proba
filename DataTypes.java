
public class DataTypes {
public static void main(String args[])
{
	//character
	char a = 'C';
	//numbers
	int i=999999999;
	//short numbers
	byte b = 99;
	//decimals
	float f = 9.516351f;
	//long
	long l = 999999999;
	
	System.out.println("char: " + a);
	System.out.println("int: " + i);
	System.out.println("byte: " + b);
	System.out.println("float: " + f);
	System.out.println("long: " + l);
}
}
